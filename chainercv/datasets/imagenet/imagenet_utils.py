import os
import shutil

from chainer.dataset import download
from chainercv import utils

root = 'pfnet/chainercv/imagenet'
devkit_url = 'http://image-net.org/image/ilsvrc2014/ILSVRC2014_devkit.tgz'
synset_to_name_url = 'http://image-net.org/archive/words.txt'


def get_ilsvrc_devkit():
    data_root = download.get_dataset_directory(root)
    base_dir = os.path.join(data_root, 'ILSVRC2014_devkit')
    if os.path.exists(base_dir):
        return data_root
    download_file_path = utils.cached_download(devkit_url)
    ext = os.path.splitext(devkit_url)[1]
    utils.extractall(download_file_path, data_root, ext)
    return data_root


def get_imagenet_full_bbox_label_names(
        path=None, base_dir='auto', keep_n=2):
    if path is None:
        if base_dir == 'auto':
            data_root = download.get_dataset_directory(root)
            path = os.path.join(data_root, 'word.txt')
            if not os.path.exists(path):
                download_file_path = utils.cached_download(synset_to_name_url)
                shutil.move(download_file_path, path)

    syn_to_name = {}
    with open(path) as f:
        for l in f:
            out = l.split()
            synset_id = out[0]
            rest = out[1:keep_n + 1]

            syn_to_name[synset_id] = ','.join(rest)

    label_names = []
    for name in imagenet_full_bbox_synset_ids:
        label_names.append(syn_to_name[name])
    return label_names


